function createSun(x,y,diameter) {
	push();

	fill(255,233,83);
	stroke(255,233,83);
	strokeWeight(15);
	scale(.75);
	translate(x - 125,y - 550);
	cx = 0; cy = 0;
	ellipse(x,y, diameter); // create the circle of the sun
	t = 0;

	if (diameter == 100) {
		for(var i = 0; i < 13; i++) {
			t = t + (2*PI/13);
			cx = x + 100*cos(t);
			cy = y + 100*sin(t);
			cx2 = x + 120*cos(t);
			cy2 = y + 120*sin(t);
			line(cx, cy, cx2, cy2);
		}
	}

	pop();

}

